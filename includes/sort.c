#include "../push_swap.h"

void	sort_3(t_node **a, int size)
{
	if (size == 2)
		ra(a);
	if (size == 3)
	{
		while (check_sort(*a) == 0)
		{
			if ((*a)->num > (*a)->next->num)
				ra(a);
			else
				sa(a);
		}
	}
}